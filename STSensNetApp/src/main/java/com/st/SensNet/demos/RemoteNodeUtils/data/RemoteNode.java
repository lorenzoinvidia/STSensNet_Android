package com.st.SensNet.demos.RemoteNodeUtils.data;

import android.os.Parcel;
import android.os.Parcelable;

/**
 * class containing the information form a remote node
 */
public class RemoteNode implements Parcelable {

    /**
     * remote node id
     */
    private final int mId;

    /**
     * build a remote node with a specfic id
     * @param id remote node id
     */
    public RemoteNode(int id){
        mId=id;
    }

    protected RemoteNode(Parcel in) {
        mId = in.readInt();
    }

    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel parcel, int i) {
        parcel.writeInt(mId);
    }

    public int getId() {
        return mId;
    }

    public static final Creator<RemoteNode> CREATOR = new Creator<RemoteNode>() {
        @Override
        public RemoteNode createFromParcel(Parcel in) {
            return new RemoteNode(in);
        }

        @Override
        public RemoteNode[] newArray(int size) {
            return new RemoteNode[size];
        }
    };
}
